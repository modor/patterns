package io.modor.patterns.flyweight;

public class ImplObject implements BaseObject {

	private String id;
	private int data;
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public ImplObject(String id) {
		this.id = id;
	}
	/**
	 * @return the data
	 */
	public int getData() {
		return data;
	}
	/**
	 * @param data the data to set
	 */
	public void setData(int data) {
		this.data = data;
	}


}
